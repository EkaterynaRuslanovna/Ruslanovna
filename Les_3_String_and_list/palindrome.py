"""

1. Користувач вводить слово, якщо це слово є поліндромом, вивести '+', інакше '-'

"""
import re


word = input("Введіть слово: ")

if not word:
    print("Слово не може бути порожнім")
elif re.search('[^a-zA-Zа-яА-ЯіІїЇ-]', word):
    print("Слово не може містити цифри та символи")
elif word == word[::-1]:
    print("+")
else:
    print("-")
